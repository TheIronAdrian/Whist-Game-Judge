void PrintCard(char cardValue, char cardColor) {
    int cardIndex;
    if(cardValue >= '2' && cardValue <= '9') {
        cardIndex = cardValue - '2' + 1;
    } else {
        switch (cardValue) {
            case 'A': cardIndex = 0; break;
            case 'T': cardIndex = 9; break;
            case 'J': cardIndex = 10; break;
            case 'Q': cardIndex = 11; break;
            case 'K': cardIndex = 12; break;
            default:
                break;
        }
    }
    int colorCode;
    if(cardColor == SPADES || cardColor == CLUBS) {
        colorCode = 30;
    } else {
        colorCode = 31;
    }
    for(int i = 0; i < CARD_HEIGHT; i++) {
        for(int j = 0; j < CARD_WIDTH; j++) {
            char c = (card[cardIndex][i][j] == 'X' ? cardColor : card[cardIndex][i][j]);
            printf("\033[%d;47m%c", colorCode, c);
        }
        printf("\033[0m\n");
    }
}